package algoritmos;

public class BingoAB2 {    
    
    public static void main(String[] args) {
        
        int fila[] = new int[5];
                
        fila[0] = 8;  // 1 - 15
        fila[1] = 18; // 16 - 30
        fila[2] = 28; // 31 - 45        
        fila[3] = 55; // 46 - 60
        fila[4] = 70; // 61 - 75
        
        System.out.println("Contenido del índice 0 -> " + fila[0]);
        System.out.println("Contenido del índice 1 -> " + fila[1]);
        System.out.println("Contenido del índice 2 -> " + fila[2]);
        System.out.println("Contenido del índice 3 -> " + fila[3]);
        System.out.println("Contenido del índice 4 -> " + fila[4]);
            
    }
    
}
